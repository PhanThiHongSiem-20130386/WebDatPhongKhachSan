const contactModel = require("../config/db/models/Contact");
class ContactController {
  get(req, res) {
    let result = contactModel.get_all();
    result
      .then(function (value) {
        console.log(value);
        res.json(value);
      })
      .catch(function (error) {
        console.log(error);
      });
  }
  find(req, res) {
    let result = contactModel.find(req.params.id);
    result
      .then(function (value) {
        console.log(value);
        res.json(value);
      })
      .catch(function (error) {
        console.log(error);
      });
  }
  create(req, res) {
    const data = {
      idAccount: req.body.idAccount,
      name: req.body.name,
      email: req.body.email,
      topic: req.body.topic,
      content: req.body.content,
    };
    let result = contactModel.create(data);
    result
      .then(function (value) {
        console.log(value);
        res.json(value);
      })
      .catch(function (error) {
        console.log(error);
      });
  }
  update(req, res) {
    const data = {
      idAccount: req.body.idAccount,
      name: req.body.name,
      email: req.body.email,
      topic: req.body.topic,
      content: req.body.content,
    };
    let result = contactModel.update(req.params.id, data);
    result
      .then(function (value) {
        console.log(value);
        res.json(value);
      })
      .catch(function (error) {
        console.log(error);
      });
  }
  delete(req, res) {
    let result = contactModel.delete(req.params.id);
    result
      .then(function (value) {
        console.log(value);
        res.json(value);
      })
      .catch(function (error) {
        console.log(error);
      });
  }
}

module.exports = new ContactController();
